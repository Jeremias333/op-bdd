class Calculator():
    def __init__(self) -> None:
        pass

    
    def sum(self, a, b):
        return a+b

    def sub(self, a, b):
        return a-b
    
    def mult(self, a, b):
        return a*b
    
    def div(self, a, b):
        return a/b
    
    def pot(self, a, b):
        return a**b